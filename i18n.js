/**
 * i18n Logic
 */
class I18nManager {
    constructor() {
        this.lang = localStorage.getItem('selectedLang') || 'ko';
        this.init();
    }

    init() {
        document.addEventListener('DOMContentLoaded', () => {
            this.applyTranslations();
            this.setupButtons();
            this.initModalLogic();
            this.initMobileMenu();
            this.initAnimations();
            this.initAntiGravity();
        });
    }

    initAntiGravity() {
        // 1. Reveal on scroll (Intersection Observer)
        const observerOptions = {
            threshold: 0.15,
            rootMargin: '0px 0px -50px 0px'
        };

        const revealObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('active');
                }
            });
        }, observerOptions);

        document.querySelectorAll('.reveal-up').forEach(el => {
            revealObserver.observe(el);
        });

        // 2. Parallax Effect on Scroll
        window.addEventListener('scroll', () => {
            const scrolled = window.pageYOffset;
            
            // Hero parallax
            const heroImg = document.querySelector('.floating-hand');
            if (heroImg) {
                heroImg.style.transform = `translateY(${scrolled * 0.1}px) rotate(${scrolled * 0.02}deg)`;
            }

            // General parallax images
            document.querySelectorAll('.parallax-img').forEach(img => {
                const speed = 0.05;
                const rect = img.getBoundingClientRect();
                const offset = rect.top * speed;
                img.style.transform = `translateY(${offset}px) rotate(${offset * 0.1}deg)`;
            });
        });
    }

    initModalLogic() {
        const productModal = document.getElementById('product-modal');
        const closeProductBtn = document.getElementById('btn-close-product');
        const viewMoreBtns = document.querySelectorAll('.btn-view-more');

        if (productModal && viewMoreBtns.length > 0) {
            viewMoreBtns.forEach(btn => {
                btn.addEventListener('click', (e) => {
                    e.preventDefault();
                    const product = btn.getAttribute('data-product');
                    
                    const titleEl = document.getElementById('modal-product-title');
                    const headlineEl = document.getElementById('modal-product-headline');
                    const descEl = document.getElementById('modal-product-desc');
                    
                    if (product === 'v4') {
                        titleEl.innerText = this.t('modal-v4-title');
                        headlineEl.innerText = this.t('modal-v4-headline');
                        descEl.innerHTML = this.t('desc-v4-full');
                    } else if (product === 'touch') {
                        titleEl.innerText = this.t('modal-touch-title');
                        headlineEl.innerText = this.t('modal-touch-headline');
                        descEl.innerHTML = this.t('desc-touch-full');
                    }
                    
                    productModal.classList.add('active');
                    document.body.style.overflow = 'hidden';
                });
            });

            if (closeProductBtn) {
                closeProductBtn.addEventListener('click', () => {
                    productModal.classList.remove('active');
                    document.body.style.overflow = 'auto';
                });
            }

            window.addEventListener('click', (e) => {
                if (e.target === productModal) {
                    productModal.classList.remove('active');
                    document.body.style.overflow = 'auto';
                }
            });
        }
    }

    initMobileMenu() {
        const mobileMenu = document.getElementById('mobile-menu');
        const navList = document.getElementById('nav-list');
        if (mobileMenu && navList) {
            mobileMenu.onclick = () => navList.classList.toggle('active');
            navList.querySelectorAll('a').forEach(link => {
                link.onclick = () => navList.classList.remove('active');
            });
        }
    }

    initAnimations() {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('fade-in');
                    observer.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1 });

        document.querySelectorAll('.hand-card, .community-cta, .hand-grid').forEach(el => {
            el.classList.add('animate-up');
            observer.observe(el);
        });
    }

    setLanguage(lang) {
        this.lang = lang;
        localStorage.setItem('selectedLang', lang);
        this.applyTranslations();
        this.updateButtonsUI();

        // Update html lang attribute
        document.documentElement.lang = lang;

        // Dispatch event for other scripts to react
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[this.lang][key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translations[this.lang][key];
                } else {
                    el.innerHTML = translations[this.lang][key];
                }
            }
        });

        // Update Page Title and Meta Description
        if (translations[this.lang]["page-title"]) {
            document.title = translations[this.lang]["page-title"];
        }
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc && translations[this.lang]["page-desc"]) {
            metaDesc.setAttribute('content', translations[this.lang]["page-desc"]);
        }

        // Special handling for elements with .kor and .eng classes if needed
        // but since we are using data-i18n, we might not need them as much.
        // However, some elements might need visibility toggling.
        const korElements = document.querySelectorAll('.kor-only');
        const engElements = document.querySelectorAll('.eng-only');

        if (this.lang === 'ko') {
            korElements.forEach(el => el.style.display = '');
            engElements.forEach(el => el.style.display = 'none');
        } else {
            korElements.forEach(el => el.style.display = 'none');
            engElements.forEach(el => el.style.display = '');
        }
    }

    setupButtons() {
        const btnKo = document.getElementById('lang-ko');
        const btnEn = document.getElementById('lang-en');

        if (btnKo && btnEn) {
            btnKo.addEventListener('click', () => this.setLanguage('ko'));
            btnEn.addEventListener('click', () => this.setLanguage('en'));
            this.updateButtonsUI();
        }
    }

    updateButtonsUI() {
        const btnKo = document.getElementById('lang-ko');
        const btnEn = document.getElementById('lang-en');

        if (btnKo && btnEn) {
            if (this.lang === 'ko') {
                btnKo.classList.add('active');
                btnEn.classList.remove('active');
            } else {
                btnKo.classList.remove('active');
                btnEn.classList.add('active');
            }
        }
    }

    /**
     * Get translation by key
     */
    t(key) {
        return translations[this.lang][key] || key;
    }
}

const i18n = new I18nManager();
window.i18n = i18n;
